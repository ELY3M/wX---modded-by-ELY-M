	float angle;  // this and one below were init assigned 0.0f
	float angle_v;
	int level       = 0;
	int level_count = 0;
	float bin_start;  // was init assigned 0.0f
	int bin = 0;

	int color_for = 0;
	float PI = 3.1415926f;

	jfloat* rBuff =  (*env)-> GetDirectBufferAddress(env,rad_buff);
	jbyte* cBuff =  (*env)-> GetDirectBufferAddress(env,color_buff);
