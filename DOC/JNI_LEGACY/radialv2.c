	unsigned char* c99_r =  (*env)-> GetDirectBufferAddress(env,colormap99_r);
	unsigned char* c99_g =  (*env)-> GetDirectBufferAddress(env,colormap99_g);
	unsigned char* c99_b =  (*env)-> GetDirectBufferAddress(env,colormap99_b);

