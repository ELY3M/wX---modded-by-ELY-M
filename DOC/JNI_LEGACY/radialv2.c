				r=bin_start + ( bin_size * level_count );
				t=angle;
				XY2[0] = r * cos(t/(180.00f / PI));
				XY2[1] = r * sin(t/(180.00f / PI));

				r= bin_start + ( bin_size * level_count );
				t=angle - angle_v ;
				XY3[0] = r * cos(t/(180.00f / PI));
				XY3[1] = r * sin(t/(180.00f / PI));

				r= bin_start;
				t=angle - angle_v ;
				XY4[0] = r * cos(t/(180.00f / PI));
				XY4[1] = r * sin(t/(180.00f / PI));

